# Operadores Aritmeticos
 
print(3 + 4) # Suma
print(3 - 4) # Resta
print(3 * 4) # Multiplicacion
print(3 / 4) # Divicion
print(100 % 3) # Modulo o Porcentaje
print(24 // 4) # Divicion de piso
print(3 ** 4) # Exponencial

print("Hola " + "Python") # Concatenar
print("Hola " + str(5))
print("Hola " * 5) # Multiplica el texto

# Operadores Comparativos

print(3 > 4) # Mayor que
print(3 < 4) # Menor que
print(3 >= 4) # Mayor o igual que
print(3 <= 4) # Menor o igual que
print(3 == 4) # Igual que
print(3 != 4) # Diferente que

print("hola" > "python") # Mayor que - Orden alfabetica ASCII
print("hola"  < "python") # Menor que - Orden alfabetica ASCII
print("hola"  >= "python") # Mayor o igual que - Orden alfabetica ASCII
print("hola"  <= "python") # Menor o igual que - Orden alfabetica ASCII
print("hola"  == "python") # Igual que - Orden alfabetica ASCII
print("hola"  != "python") # Diferente que - Orden alfabetica ASCII


# Operadores Logicos

print(3 > 4 and "hola" > "python") # Y
print(3 > 4 or "hola" > "python") # O
print(not 3 < 4) # NO